namespace TheNote\Leaderboard;

use pocketmine\plugin\PluginBase;
use pocketmine\utils\Config;
use TheNote\Leaderboard\Task\LeaderboardTask;

class Main extends PluginBase
{
    public $anni = 1;

    public function onLoad(): void
    {
        $projectcore = $this->getServer()->getPluginManager()->getPlugin("ProjectCore");
        if ($projectcore === null) {
            $this->getLogger()->alert("This Plugin need ProjectCore! Please install ProjectCore before Using this Plugin!");
            $this->getServer()->shutdown();
        }
        $this->saveResource("Leaderboard.yml");
    }
    public function onEnable(): void
    {
        $cfg = new Config($this->getDataFolder() . "Leaderboard.yml", Config::YAML);
        $this->getScheduler()->scheduleRepeatingTask(new LeaderboardTask($this), $cfg->get("Duration"));
    }
}

